
/* 
Polymorphism is one of the core principles of Object-Oriented Programming (OOP) that allows objects of different classes to be treated as objects of a common superclass. 
It enables a single interface to be used for different data types, leading to code reusability and scalability.

In JavaScript, polymorphism can be achieved through:

Method Overriding (Runtime Polymorphism)

Method Overloading (JavaScript does not support it directly, but it can be simulated)

Duck Typing (Objects with similar behavior can be used interchangeably)

*/