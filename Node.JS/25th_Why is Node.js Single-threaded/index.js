
// Why is Node.js Single-threaded :

/* 
Simplification of Development

Avoiding Complexity: 
Multithreading introduces complexity in development due to issues like deadlocks, 
race conditions, and difficulties in managing concurrent access to shared resources. 
By being single-threaded, Node.js avoids these complexities, making it easier for developers to write and maintain code.

Event-Driven Architecture: 

Node.js leverages an event-driven architecture, which simplifies handling asynchronous operations. 
In an event-driven system, the flow of the program is determined by events like user actions, I/O operations, or messages from other programs.
*/