
// Why we should use TypeScript

/* 
TypeScript has additional features, which does not exist in current version of JavaScript.
It makes writing JavaScript code much easier, faster and less error prone.
*/

/* 
TypeScript is not just a programming language. It is also a very powerful compiler which can run and compile TypeScript code into JavaScript.
*/


/* 
When we run a TypeScript code, it gets compiled to JavaScript code, Which can be executed by JavaScript runtime like browsers.
*/